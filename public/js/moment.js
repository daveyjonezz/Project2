
var currentTime = new Date().getHours();
if (6 <= currentTime && currentTime < 10) {
    if (document.body) {
        document.body.background = "../assets/images/backgrounds/calm-morning.png";
    }
}
else if (10 <= currentTime && currentTime < 17) {
    if (document.body) {
        document.body.background = "../assets/images/backgrounds/calm-morning.png";
    }
}
else if (17 <= currentTime && currentTime < 20) {
    if (document.body) {
        document.body.background = "../assets/images/backgrounds/calm-morning.png";
    }
}
else if (20 <= currentTime && currentTime < 5) {
    if (document.body) {
        document.body.background = "../assets/images/backgrounds/calm-morning.png";
    }
}
